#creating a list of places i want to visit then printing
places = ['Spain', 'Portugual', 'Italy', 'Greece', 'Vietnam']
print(places)

#using the function sorted to print the list in alphabetical order without modifying the list
print(sorted(places))
print(places)

#using sorted to print the list in reverse alphabetical order without changing the order of the orginal list
print(sorted(places, reverse=True))
print(places)

#using reverse to cahnge the order of the list then printing the list to show that the order has changed
places.reverse()
print(places)

#using reverse to change back to orginal order then printing
places.reverse()
print(places)

#using sort to change my list so its tored in alphabetical order
places.sort()
print(places)

#using sort to change the order of my list again but in reverse alphabetical order
places.sort(reverse=True)
print(places)




